F_name = "vladimir"  # Создаём переменную с именем
L_name = "ivanov"  # Создаём переменную с фамилией
print(F_name.lower() + " " + L_name.lower())  # Выводим имя и фамилию заглавными буквами
print(F_name.upper() + " " + L_name.upper())  # Выводим имя и фамилию маленькими буквами
print(F_name.title() + " " + L_name.title())  # Выводим имя и фамилию с первыми заглавными буквами
